package com.hashtable;

public class Node {

	Integer data;
	Node nextRef;
	
	public Integer getData() {
		return data;
	}
	public void setData(Integer data) {
		this.data = data;
	}
	public Node getNextRef() {
		return nextRef;
	}
	public void setNextRef(Node nextRef) {
		this.nextRef = nextRef;
	}
	
	
}
